    public function run()
{
    $lastLevel1Code = null; // Lưu code của bản ghi cấp 1 gần nhất
    $lastInsertedCode = null; // Lưu code của bản ghi được insert trước đó (dùng để debug)

    foreach ($this->data as $index => $row) {
        $description = $row[5]; // Mô tả luôn ở cột cuối
        $code = '';
        $level = 0;
        $parent_code = null;

        // Xác định code và level, ưu tiên cấp 4 nếu có
        if (!empty($row[3])) {
            $code = $row[3]; // Mã cấp 4
            $level = 4;
        } elseif (!empty($row[0])) {
            $code = $row[0]; // Mã cấp 1
            $level = 1;
        } elseif (!empty($row[1])) {
            $code = $row[1]; // Mã cấp 2
            $level = 2;
        } elseif (!empty($row[2])) {
            $code = $row[2]; // Mã cấp 3
            $level = 3;
        } elseif (!empty($row[4])) {
            $code = $row[4]; // Mã cấp 5
            $level = 5;
        } else {
            \Log::warning("Skipped invalid row at index $index: " . json_encode($row));
            continue; // Bỏ qua hàng không hợp lệ
        }

        // Kiểm tra mã trùng lặp
        if (Industry::where('code', $code)->exists()) {
            \Log::warning("Skipped duplicate code: $code at level $level, index $index");
            continue;
        }

        // Xác định parent_code
        if ($level == 1) {
            $parent_code = null; // Level 1 không có parent
            $lastLevel1Code = $code; // Lưu code cấp 1
        } elseif ($level == 2) {
            $parent_code = $lastLevel1Code; // Level 2 lấy code của cấp 1 gần nhất
            if (empty($parent_code) || !Industry::where('code', $parent_code)->exists()) {
                \Log::warning("Skipped code $code at level $level, index $index: Invalid parent_code $parent_code");
                continue;
            }
        } else {
            // Level 3 trở lên: Tìm parent_code bằng cách bỏ ký tự cuối
            $currentCode = $code;
            $foundParent = false;
            while (strlen($currentCode) > 0) {
                $currentCode = substr($currentCode, 0, -1); // Bỏ ký tự cuối
                if (!empty($currentCode) && Industry::where('code', $currentCode)->exists()) {
                    $parent_code = $currentCode;
                    $foundParent = true;
                    break;
                }
            }
            if (!$foundParent) {
                \Log::warning("Skipped code $code at level $level, index $index: No valid parent_code found");
                continue;
            }
        }

        // Chuẩn bị dữ liệu để chèn
        $params = [
            'code' => $code,
            'description' => $description,
            'level' => $level,
            'parent_code' => $parent_code,
        ];

        // Chèn dữ liệu
        try {
            Industry::insertItem($params);
            $lastInsertedCode = $code; // Cập nhật code của bản ghi vừa chèn
            if ($level == 1) {
                $lastLevel1Code = $code; // Cập nhật code cấp 1
            }
            \Log::info("Inserted code $code at level $level, parent_code: " . ($parent_code ?? 'null'));
        } catch (\Exception $e) {
            \Log::error("Error inserting code $code at level $level, index $index: " . $e->getMessage());
            continue;
        }
    }
